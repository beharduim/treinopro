/// Duração das animações e transições
class AppDurations {
  AppDurations._();

  /// Duração da splash screen (3 segundos)
  static const Duration splashDuration = Duration(seconds: 3);
  
  /// Duração de animações rápidas
  static const Duration fastAnimation = Duration(milliseconds: 300);
  
  /// Duração de animações médias
  static const Duration mediumAnimation = Duration(milliseconds: 500);
  
  /// Duração de animações lentas
  static const Duration slowAnimation = Duration(milliseconds: 1000);
}
